; PROGRAM TO BLINK LED FOR EVERY 1 SEC

ORG 0000H
MOV TMOD, #01H ; TIMER 0 MODE 1
MOV R1, #080H
ACALL BLINK

; 1S -> 1000mS ~ 14 * 71.10
LOOP_AGAIN:MOV R0, #13H
           JB P0.0, LOOP_AGAIN

AGAIN:MOV TL0, #00H
MOV TH0, #00H
SETB TR0

LOOP: JNB TF0, LOOP
      CLR TR0
      DJNZ R0, AGAIN
      SJMP LOOP_AGAIN

BLINK: CPL P0.0
       RET 
END
      